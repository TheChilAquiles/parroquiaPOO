    const $ = (id) => document.getElementById(id);

    const btnCopyLink = $('btnCopyLink');
    if (btnCopyLink) {
        btnCopyLink.addEventListener('click', async () => {
            const fbLink = 'https://www.facebook.com/francisco.deasis.79274';
            try {
                await navigator.clipboard.writeText(fbLink);
                showToast('Enlace de Facebook copiado');
            } catch (error) {
                showToast('No se pudo copiar el enlace');
            }
        });
    }

    const direccion = $('direccion')?.innerText;
    if (direccion) {
        const mapsUrl = 'https://www.google.com/maps/search/?api=1&query=' + encodeURIComponent(direccion);
        if($('btnMapa')) $('btnMapa').href = mapsUrl;
        if($('modalMaps')) $('modalMaps').href = mapsUrl;
    }

    const telElement = $('telefono');
    if (telElement) {
        const tel = telElement.innerText.replace(/\s/g, '');
        if($('btnLlamar')) $('btnLlamar').href = 'tel:' + tel;

        const btnCopiarTelefono = $('btnCopiarTelefono');
        if (btnCopiarTelefono) {
            btnCopiarTelefono.addEventListener('click', async () => {
                try {
                    await navigator.clipboard.writeText(tel);
                    showToast('Número copiado al portapapeles');
                } catch (error) {
                    showToast('No se pudo copiar el número');
                }
            });
        }
    }

    const btnContactarWhatsApp = $('btnContactarWhatsApp');
    if (btnContactarWhatsApp) {
        btnContactarWhatsApp.addEventListener('click', () => {
            showToast('Redirigiendo a WhatsApp...');
            setTimeout(() => {
                window.open('https://wa.me/573014023526', '_blank');
            }, 500);
        });
    }

    const modal = $('modal');
    const openModal = () => {
        if(modal) {
            modal.classList.remove('hidden');
            modal.classList.add('flex');
            document.body.style.overflow = 'hidden';
        }
    };
    const closeModal = () => {
        if(modal) {
            modal.classList.add('hidden');
            modal.classList.remove('flex');
            document.body.style.overflow = 'auto';
        }
    };

    if($('btnDetalles')) $('btnDetalles').addEventListener('click', openModal);
    if($('closeModal')) $('closeModal').addEventListener('click', closeModal);
    if($('closeModal2')) $('closeModal2').addEventListener('click', closeModal);

    if(modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) closeModal();
        });
    }

    function showToast(message) {
        const toast = $('toast');
        if(!toast) return;
        
        const messageElement = toast.querySelector('.toast-message');
        if(messageElement) messageElement.textContent = message;

        toast.classList.remove('hidden');

        clearTimeout(showToast._timeout);
        showToast._timeout = setTimeout(() => {
            toast.classList.add('hidden');
        }, 3000);
    }

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal && !modal.classList.contains('hidden')) {
            closeModal();
        }
    });
</script>